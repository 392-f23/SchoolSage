export const GetMessageData = () => {
  const messageDataMap = new Map();

  chatListData.forEach((item, index) => {
    if (!messageDataMap.has(item.title)) {
      messageDataMap.set(item.title, []);
    }

    let messages = messageDataMap.get(item.title);

    messages.push({
      position: "left",
      type: "text",
      title: item.title,
      text: item.subtitle,
    });

    const additionalMessages = Math.floor(Math.random() * 3);

    for (let i = 0; i < additionalMessages; i++) {
      const position = Math.random() > 0.5 ? "left" : "right";
      const text =
        position === "left"
          ? fakeRepliesLeft[Math.floor(Math.random() * fakeRepliesLeft.length)]
          : fakeRepliesRight[
              Math.floor(Math.random() * fakeRepliesRight.length)
            ];

      messages.push({
        position: position,
        type: "text",
        title: position === "left" ? item.title : "You",
        text: text,
      });
    }

    messageDataMap.set(item.title, messages);
  });

  return messageDataMap;
};
